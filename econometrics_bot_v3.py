async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data

    if data == "menu":
        await query.edit_message_text(
            "🏠 *Выбери вопрос:*",
            reply_markup=main_keyboard(),
            parse_mode='Markdown'
        )

    elif data == "noop":
        pass

    elif data.startswith("q_"):
        q_num = int(data.split("_")[1])
        q = QUESTIONS[q_num]
        await query.edit_message_text(
            q["theory"],
            reply_markup=question_keyboard(q_num),
            parse_mode='Markdown'
        )

    elif data.startswith("det_"):
        q_num = int(data.split("_")[1])
        q = QUESTIONS[q_num]
        await query.edit_message_text(
            q["details"],
            reply_markup=detail_keyboard(q_num),
            parse_mode='Markdown'
        )

    elif data.startswith("code_"):
        q_num = int(data.split("_")[1])
        q = QUESTIONS[q_num]
        title = q["title"]
        text = f"💻 *Код Python — Вопрос {q_num}: {title}*\n\n{q['code']}"
        await query.edit_message_text(
            text,
            reply_markup=code_keyboard(q_num),
            parse_mode='Markdown'
        )

